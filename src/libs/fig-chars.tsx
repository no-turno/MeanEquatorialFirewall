import { renderToString } from "react-dom/server";

export async function renderFigChars() {
    const rand =  () => Math.floor(Math.random() * 10) + 10;
    const length = rand();
    const chars = Array.from({
		length
	})
		.map(() =>
			Array.from({
				length,
			}).map((_, i) => (
				<div className="h-2 w-2 bg-black animate-spin transition-all decoration-2 border-y-indigo-900 duration-700 ease-in-out bg-blue-300" key={i}>
					{String.fromCharCode(Math.floor(length * 26) * Math.floor(Math.random() * 26) + 65).toLowerCase()
                        .replace(/[a-z]/g, () => String.fromCharCode(Math.floor(
                            Math.random() * 26) + 65))
                    }
				</div>
			)),
		)


	return <div>{await Bun.$`bunx figlet-cli '${chars}'`.text()}</div>
}


